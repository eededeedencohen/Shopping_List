/**
 * Pre-build step for `npm run deploy`.
 *
 *   1. Reads the current "version" from this project's package.json.
 *   2. Bumps the patch number (1.1.0 → 1.1.1 → 1.1.2 …).
 *   3. Writes the new version back into package.json.
 *   4. Writes src/buildInfo.js — a tiny module that the React bundle
 *      imports. The version is therefore BAKED INTO THE JS BUNDLE at
 *      build time; the running app does not query any server or DB to
 *      know what version it is. That is the only reliable way to tell
 *      from the phone whether the device has the fresh deploy.
 *
 * Run order in deploy:
 *   bump-build-version  →  react-scripts build  →  sync-to-backend
 */
const fs = require("fs");
const path = require("path");

const PROJECT_ROOT = path.resolve(__dirname, "..");
const PKG_PATH = path.join(PROJECT_ROOT, "package.json");
const OUT_PATH = path.join(PROJECT_ROOT, "src", "buildInfo.js");

function bumpPatch(version) {
  const parts = String(version || "0.0.0")
    .split(".")
    .map((n) => parseInt(n, 10) || 0);
  while (parts.length < 3) parts.push(0);
  parts[2] += 1;
  return parts.slice(0, 3).join(".");
}

const pkgRaw = fs.readFileSync(PKG_PATH, "utf-8");
const pkg = JSON.parse(pkgRaw);
const prev = pkg.version || "0.0.0";
const next = bumpPatch(prev);

// Write package.json back preserving 2-space indent + trailing newline.
pkg.version = next;
fs.writeFileSync(PKG_PATH, JSON.stringify(pkg, null, 2) + "\n", "utf-8");

const now = new Date().toISOString();
const banner =
  "// Auto-generated by scripts/bump-build-version.js — do not edit by hand.\n" +
  "// Bumped automatically by `npm run deploy` so the running bundle\n" +
  "// carries an unambiguous version string.\n";
fs.writeFileSync(
  OUT_PATH,
  banner +
    `export const BUILD_VERSION = ${JSON.stringify(next)};\n` +
    `export const BUILD_TIMESTAMP = ${JSON.stringify(now)};\n`,
  "utf-8"
);

console.log(`[bump-build-version] ${prev} → ${next}  (built at ${now})`);
