const MonthNamesShort = [
  "Jan",
  "Feb",
  "Mar",
  "Apr",
  "May",
  "Jun",
  "Jul",
  "Aug",
  "Sep",
  "Oct",
  "Nov",
  "Dec",
];

const MonthlyExpensesChart = () => {
  const months = useMemo(() => {
    const map = {};
    allHistory.forEach((h) => {
      const d = new Date(h.date);
      const key = `${d.getFullYear()}-${d.getMonth()}`; // 2023-5
      map[key] = (map[key] || 0) + (h.totalPrice || 0);
    });

    return Object.entries(map)
      .map(([key, total]) => {
        const [year, monthIdx] = key.split("-");
        return {
          key: +year * 12 + +monthIdx, // for numeric sort
          label: `${MonthNamesShort[monthIdx]} ${year.slice(-2)}`,
          total,
        };
      })
      .sort((a, b) => a.key - b.key);
  }, []);

  const max = Math.max(...months.map((m) => m.total), 1);

  return (
    <section className="expenses-chart">
      {months.map(({ label, total }) => (
        <div className="bar-wrapper" key={label}>
          <div
            className="bar"
            style={{ "--bar-height": `${(total / max) * 100}%` }}
          />
          <span className="bar-label">{label}</span>
        </div>
      ))}
    </section>
  );
};

