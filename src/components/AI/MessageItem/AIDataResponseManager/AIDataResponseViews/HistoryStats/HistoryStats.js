const HistoryStats = () => {
  const stats = useMemo(() => {
    const totalCarts = allHistory.length;
    const totalSpent = allHistory.reduce(
      (sum, h) => sum + (h.totalPrice || 0),
      0
    );
    const avgSpent = totalCarts ? totalSpent / totalCarts : 0;

    // Identify the most‑purchased product (by units).
    const productUnits = {};
    allHistory.forEach(({ products = [] }) => {
      products.forEach((p) => {
        productUnits[p.generalName] =
          (productUnits[p.generalName] || 0) + p.amount;
      });
    });
    const [topProductName, topProductAmount] = Object.entries(
      productUnits
    ).sort((a, b) => b[1] - a[1])[0] || ["—", 0];

    return {
      "סך‑הכל סלים": totalCarts,
      "הוצאה מצטברת": `₪ ${totalSpent.toFixed(2)}`,
      "ממוצע לסל": `₪ ${avgSpent.toFixed(2)}`,
      "המוצר הכי נקנה": `${topProductName} (${topProductAmount})`,
    };
  }, []);

  return (
    <section className="history-stats">
      {Object.entries(stats).map(([label, value]) => (
        <div className="stat-card" key={label}>
          <div className="stat-value">{value}</div>
          <div className="stat-label">{label}</div>
        </div>
      ))}
    </section>
  );
};

